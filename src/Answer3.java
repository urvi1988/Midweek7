public class Answer3 {
    //3. Write an infinite loop.
    //A infinite loop never ends. Condition is always true.

    public static void main(String[] args) { // predefine method

        int A=100; // define variable
        do {
            System.out.println("Infinite Loop"); // print statment
            System.out.println(A);
            A++;
        } while (A>=100); // do while loop method
    }
}
